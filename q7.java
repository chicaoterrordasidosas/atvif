public class QuestaoSete {
    public static void main(String[] args) {
        // Estrutura sequencial
        System.out.println("Hello World");
        System.out.println("Hello World");
        System.out.println("Hello World");
        System.out.println("Hello World");
        System.out.println("Hello World");
        System.out.println("Hello World");
        System.out.println("Hello World");
        System.out.println("Hello World");
        System.out.println("Hello World");
        System.out.println("Hello World");
        System.out.println("Hello World");
        
        // Estrutura de repetição while
        int i = 0;
        while (i < 11) {
            System.out.println("Hello World");
            i++;
        }
    }
}
